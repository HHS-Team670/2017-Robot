package org.usfirst.frc.team670.robot;
/**
 * The RobotMap is a mapping from the ports sensors and actuators are wired into
 * to a variable name. This provides flexibility changing wiring, makes checking
 * the wiring easier and significantly reduces the number of magic numbers
 * floating around.
 */
public class RobotMap {
    //DriveBase
    public static int leftMotor1 = 3;
    public static int leftMotor2 = 4;
    public static int rightMotor1 = 2;
    public static int rightMotor2 = 1;
    public static int omniWheel = 5;
    
    public static int intakeMotor = 6;
    public static int dumperMotor = 7;
    public static int grapplerMotor = 8;
    
    public static int UltrasonicAI = 0;

    //Joysticks + Buttons
    public static int operatorStick = 0;
    public static int leftDriveStick = 1;
    public static int rightDriveStick = 2;
    public static int arcButtons = 3;
	
    

    
}
